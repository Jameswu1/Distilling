        for i in range(4):
            func = getattr(self, 'enc_{:d}'.format(i + 1))
            results.append(func(results[-1]))
        #func = getattr(self, 'enc_{:d}'.format(4 + 1))
        #results.append(func(results[-1]))
        return results[1:]

